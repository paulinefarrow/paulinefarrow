# declare a variable sentence to hold the string

sentence = "The!quick!brown!fox!jumps!over!the!lazy!dog"

# print the string with spaces instead of exclamation marks using the replace() function

print(sentence.replace('!',' '))

# print the sentence in upper case using the upper() function

print((sentence.replace('!',' ')).upper())

# print the sentence in reverse

print((sentence.replace('!',' '))[::-1])